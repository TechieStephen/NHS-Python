#Python Operators
'''
Operators are used to perform operations
on variables and values.

---------------------------------------
Arithmetic Operators
in python we can use a lot of mathematical
operators

- addition +
- subtraction -
- division /
- floor division //
- Exponentiation **
- multiplication *
- modulus % (10 % 4) which is read as 10 mod 4


We can also perform more complex mathematical
operations
e.g PEDMAS
for example
5 * 2 + 20

5 and 2 gets multiplied first before the result is added to 20

BUT in the equation
5 * (2 + 20)

2 and 20 gets added first before the result is multiplied by 5
this is because we are introduction a bracket/parentheses  which changes
the order of operation
so we can use bracket/parentheses to change order of operation



------------------------------
Assignment Operators
Assignment operators are used to assign values to variables:
=, +=, -+, *=, /=, %=, //=, **=,



-------------------------
Comparison Operators
Comparison operators are used to compare two values:

==	Equal	x == y
!=	Not equal	x != y
>	Greater than	x > y
<	Less than	x < y
>=	Greater than or equal to	x >= y
<=	Less than or equal to	x <= y


---------------------------------
Identity Operators
Identity operators are used to compare the
objects, not if they are equal, but if they
are actually the same object, with the same
memory location:

- is 	Returns true if both variables are
        the same object	x is y

- is not	Returns true if both variables
            are not the same object	x is not y



-------------------------------
Logical Operators
Logical operators are used to combine
conditional statements:

and 	Returns True if both statements are true
        x < 5 and  x < 10
or	    Returns True if one of the statements is true
        x < 5 or x < 4
not	    Reverse the result, returns False if the
        result is true
        not(x < 5 and x < 10)
'''

