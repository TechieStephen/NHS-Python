#Variables
'''
Variables are containers for storing or
holding values

-CREATING VARIABLES
---Python has no command for creating variables,
a variable is created the moment you assign a
value to it.

x = 5
y = "John"
print(x)
print(y)

RULES TO FOLLOW WHEN NAMING VARIABLES
--------------------------------------------------
- A variable name must start with a letter
or the underscore character

- A variable name cannot start with a number

- A variable name can only contain alpha-numeric
characters and underscores (A-Z, a-z, 0-9, and _ )

- Variable names are case-sensitive
(age, Age and AGE are three different
variables)
'''

name = "James Peter"
age = "60"
story = "There is a farmer named " + name + ", he is "+ age +" years old, "+ name +" likes farming, but he hates being "+ age +""
print(story)
print("-----------------------------------------")

length = 20
breath = 5
area = length * breath
print(area)
print("-----------------------------------------")

x = 20
print(x)

X = "Hello World"
print(X)

print("-----------------------------------------")

